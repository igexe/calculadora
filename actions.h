#include <stdio.h>
#include <stdlib.h>

float soma(float res,float num1){
	float op=res+num1;
	return op;
}

float subtrai(float res,float num1){
	float op=res-num1;
	return op;
}

float multiplica(float num1,float num2){
	float op=num1*num2;
	return op;
}

float divide(float num1,float num2){
	float op=num1/num2;
	return op;
}
